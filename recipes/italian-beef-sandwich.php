                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>The next day, remove roast from the pan, and slice as thinly as possible. Add a little water to the roasting pan, and heat on the stovetop, but do not boil. Stir to blend seasonings.</p>
