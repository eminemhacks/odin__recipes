                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Preheat the oven to 250 degrees F (120 degrees C).</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Make slits in roast with a sharp knife, and insert garlic slivers. Place roast in a pan not much larger than the roast. Pour water into the pan, and season roast with salt, black pepper, red pepper, and oregano.</p>
                </div><br>
                <div class="info-row">
