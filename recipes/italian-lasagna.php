                  <div class="border-0 shadow-sm">
                      <!-- Ingredients -->
                      <div class="row g-2">
                          <h3>Italian Lasagna Ingredients:</h3>
                          <h4>Sauce and Sausage:</h4>
                          <p>9 thick slices bacon, diced</p>
                          <p>1 medium onion, chopped</p>
                          <p>2 (28 ounce) cans tomato sauce</p>
                          <p>1 ½ teaspoons Italian seasoning</p>
                          <p>1 teaspoon fennel seed</p>
                          <p>1 teaspoon dried oregano</p>
                          <p>2 pounds Italian sausage links</p>
                          <h4>Lasagna:</h4>  
                          <p>2 pints part-skim ricotta cheese</p>
                          <p>⅓ cup milk</p>
                          <p>2 large eggs</p>
                          <p>2 teaspoons chopped fresh parsley</p>
                          <p>1 teaspoon dried oregano</p>
                          <p>1 (16 ounce) package uncooked lasagna noodles</p>
                          <p>8 slices provolone cheese</p>
                          <p>6 cups shredded mozzarella cheese</p>
                      </div>
                  </div>
                </div>

              <!-- Content Column -->
              <div class="col-12 col-lg-8 text-white p-2" data-aos="fade-up" data-aos-delay="200">
                <h2 class="text-uppercase text-center text-lg-start">Directions</h2>
                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Make the sauce: Brown bacon and onion in a large pan over medium heat until bacon is crisp and onion is translucent, 7 to 9 minutes. Stir in tomato sauce, Italian seasoning, fennel seed, and oregano. Reduce the heat to low, cover, and simmer until thick, 4 to 6 hours..</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Cook the sausage: When the sauce is almost ready, brown sausage links in a large skillet. Drain on paper towels and cut into 1-inch pieces.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 3:</span> <br>Make the lasagna: Mix ricotta cheese, milk, eggs, parsley, and oregano together in a medium bowl.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>Spread 1 cup sauce over the bottom of a 9x13-inch pan. Layer with 1/3 of the lasagna noodles, 1/2 of the ricotta mixture, 1/2 of the sausage pieces, 2 cups mozzarella, 4 slices provolone, and 1/3 of the sauce. Repeat layers once more, then top with remaining noodles. Spread remaining sauce over noodles and sprinkle with remaining mozzarella.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 5:</span> <br>Bake in the preheated oven for 1 1/2 hours.</p>
                </div><br>
              </div>
