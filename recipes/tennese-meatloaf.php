          <div class="container meatloaf-info" data-aos="fade-up" data-aos-delay="150">
            <div class="row align-items-start">
                <div class=" col-12 col-lg-4 border-info">
                  <div class="border-0 shadow-sm">
                      <!-- Ingredients -->
                      <div class="row g-2">
                          <h3>Dill Pickle Meatloaf Ingredients:</h3>
                          <h4>Brown Sugar Glaze</h4>
                          <p>½ cup ketchup</p>
                          <p>¼ cup brown sugar</p>
                          <p>2 tablespoons cider vinegar</p>
                          <h4>Meatloaf</h4>
                          <p>cooking spray</p>
                          <p>1 onion, chopped</p>    
                          <p>½ green bell pepper, chopped</p>
                          <p>⅔ cup quick cooking oats</p>
                          <p>½ cup milk</p>
                          <p>2 large eggs, lightly beaten</p>
                          <p>2 cloves garlic, minced</p>
                          <p>2 teaspoons prepared mustard</p>
                          <p>2 teaspoons Worcestershire sauce</p>
                          <p>1 teaspoon dried thyme</p>
                          <p>1 teaspoon seasoned salt</p>
                          <p>½ teaspoon ground black pepper</p>
                          <p>½ teaspoon hot pepper sauce (such as Tabasco)</p>
                          <p>1 pound ground beef</p>
                          <p>½ pound ground pork</p>
                          <p>½ pound ground veal</p>
                      </div>
                  </div>
                </div>

              <!-- Content Column -->
              <div class="col-12 col-lg-8 p-2" data-aos="fade-up" data-aos-delay="200">
                <h2 class="text-uppercase text-center text-lg-start">Directions</h2>
                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Make the glaze: Mix ketchup, brown sugar, and cider vinegar together in a bowl until well combined.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Make the meatloaf: Preheat the oven to 350 degrees F (175 degrees C). Spray two 9x5-inch loaf pans with cooking spray or line with aluminum foil for easier cleanup.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 3:</span> <br>Combine onion and bell pepper in a microwave-safe container. Cover and microwave until softened, 1 to 2 minutes. Set aside to cool.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>Mix oats, milk, eggs, garlic, mustard, Worcestershire sauce, thyme, seasoned salt, black pepper, and hot sauce together in a large bowl until well combined. Stir in cooked onion and bell pepper. Add ground beef, pork, and veal. With gloved hands, work all ingredients together until completely mixed and uniform.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 5:</span> <br>Pat 1/2 of the meatloaf mixture into each prepared loaf pan. Brush each loaf with 1/4 of the glaze; set remaining glaze aside.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 6:</span> <br>Bake in the preheated oven for 50 minutes. Remove the pans from the oven and carefully drain the fat. Brush remaining glaze over loaves, then return to the oven and bake for 10 more minutes. An instant-read thermometer inserted into the center should read at least 160 degrees F (70 degrees C).</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 7:</span> <br>Remove from the oven and let stand for 15 minutes before slicing.</p>
                </div><br>
                <h4 class="tips-header text-center text-lg-start">Recipe Tips</h4>
                <div class="info-row">
                    <p>You can make one large loaf in a 9x13-inch pan. Bake for 60 to 70 minutes before brushing with the glaze, then continue to bake as directed.<br>You may also use 1 pound of ground veal and omit the pork.</p>
                    </div>
              </div>
