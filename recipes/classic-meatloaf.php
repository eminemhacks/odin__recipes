          <div class="container meatloaf-info" data-aos="fade-up" data-aos-delay="200">
            <div class="row align-items-start">
                <div class=" col-12 col-lg-4 border-info">
                  <div class="border-0 shadow-sm">
                      <!-- Ingredients -->
                      <div class="row g-2">
                          <h3>Meatloaf Ingredients:</h3>
                          <p>1 medium carrot, coarsely chopped</p>
                          <p>1 rib celery, coarsely chopped</p>                     
                          <p>½ medium onion, coarsely chopped</p>
                          <p>½ medium red bell pepper, coarsely chopped</p>
                          <p>4 white mushrooms, coarsely chopped</p>    
                          <p>3 cloves garlic, coarsely chopped</p>
                          <p>2 ½ pounds ground chuck</p>
                          <p>1 tablespoon Worcestershire sauce</p>
                          <p>1 large egg, beaten</p>
                          <p>1 teaspoon dried Italian herbs</p>
                          <p>2 teaspoons salt</p>
                          <p>1 teaspoon ground black pepper</p>
                          <p>½ teaspoon cayenne pepper</p>
                          <p>1 cup plain bread crumbs</p>   
                          <p>Glaze Ingredients:</p>
                          <p>2 tablespoons brown sugar</p>
                          <p>2 tablespoons ketchup</p>
                          <p>2 tablespoons Dijon mustard</p>
                          <p>1 teaspoon Sriracha sauce, or to taste</p>
                      </div>
                  </div>
                </div>

              <!-- Content Column -->
              <div class="col-12 col-lg-8 text-white p-2">
                <h2 class="text-uppercase text-center text-lg-start">Directions</h2>
                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Gather all ingredients.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Preheat the oven to 325 degrees F (165 degrees C). Line a baking dish with lightly greased foil.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 3:</span> <br>Place carrot, celery, onion, bell pepper, mushrooms, and garlic in a food processor; pulse until very finely chopped, almost to a purée. Transfer to a large mixing bowl.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>Add ground chuck to the vegetables, along with Worcestershire sauce, egg, Italian herbs, salt, black pepper, and cayenne. Mix gently with a wooden spoon until ingredients are just combined. Sprinkle in bread crumbs and gently mix with your fingertips until just combined; don't overmix.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 5:</span> <br>Shape the mixture into a loaf, about 4 inches high by 6 inches across. Place in the prepared baking pan.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 6:</span> <br>Bake in the preheated oven just until the meatloaf is hot, about 15 minutes.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 7:</span> <br>Meanwhile, stir brown sugar, ketchup, Dijon, and Sriracha for glaze in a small bowl until brown sugar has dissolved.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 8:</span> <br>Remove meatloaf from the oven. Spoon glaze on the top of the meatloaf with the back of a spoon, then pull a tiny bit glaze down the sides.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 9:</span> <br>Return to the oven, and bake until no longer pink inside, 45 to 75 more minutes. An instant-read thermometer inserted into the thickest part of the loaf should read at least 160 degrees F (70 degrees C), so start checking at 45 minutes and continue baking until meatloaf reaches that temperature. Cooking time will depend on shape and thickness of the meatloaf.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 10:</span> <br>Serve hot and enjoy!</p>
                </div><br>
              </div>
            </div>
