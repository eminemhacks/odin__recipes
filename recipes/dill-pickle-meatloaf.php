                      <div class="row g-2">
                          <h3>Dill Pickle Meatloaf Ingredients:</h3>
                          <p>cooking spray</p>
                          <p>1 large egg</p>
                          <p>1 small onion, chopped</p>
                          <p>½ cup dill pickle juice</p>
                          <p>¾ teaspoon salt</p>    
                          <p>¼ teaspoon ground black pepper</p>
                          <p>1 slice bread, torn into small pieces</p>
                          <p>1 pound lean ground beef</p>
                          <p>¼ cup chopped dill pickles</p>
                          <p>¼ cup ketchup</p>
                          <p>2 tablespoons water</p>
                          <p>1 tablespoon brown sugar</p>
                          <p>½ teaspoon Worcestershire sauce</p>
                      </div>
                  </div>
                </div>

              <!-- Content Column -->
              <div class="col-12 col-lg-8 text-white p-2" data-aos="fade-up" data-aos-delay="200">
