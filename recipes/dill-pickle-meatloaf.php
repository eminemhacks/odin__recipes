                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Preheat the oven to 350 degrees F (175 degrees C). Grease an 8x8-inch baking dish.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Beat egg in a mixing bowl. Whisk in onion, pickle juice, salt, and pepper. Stir in bread until soggy, then mix in ground beef until evenly combined. Form into a loaf and place into the prepared pan.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 3:</span> <br>Stir together chopped pickles, ketchup, water, sugar, and Worcestershire sauce; pour over the meatloaf.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>Bake in the preheated oven until no longer pink in the center, about 1 hour. An instant-read thermometer inserted into the center should read at least 160 degrees F (70 degrees C).</p>
