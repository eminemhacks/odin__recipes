        <section class="section recipe p-3" id="meatloaf">
          <div class="container meatloaf-info" data-aos="fade-up" data-aos-delay="150">
            <div class="row align-items-start">
                <div class=" col-12 col-lg-4 border-info">
                  <div class="border-0 shadow-sm">
                      <!-- Ingredients -->
                      <div class="row g-2">
                          <h3>Meatloaf Ingredients:</h3>
                          <p>2 cups baking mix, such as Bisquick</p>
                          <p>1 1/4 cups whole milk</p>
                          <p>2 tablespoons vegetable oil, divided</p>
                          <p>2 pounds lean ground beef</p>
                          <p>1 onion, chopped</p>  
                          <p>2 poblano peppers, seeded and chopped</p>  
                          <p>1 packet taco seasoning</p>  
                          <p>1/2 teaspoon kosher salt</p>  
                          <p>1/2 cup mayonnaise</p> 
                          <p>1/2 cup sour cream, plus more for garnish</p> 
                          <p>2 cups shredded pepper Jack cheese, divided</p> 
                          <p>2 cups shredded Cheddar cheese, divided</p> 
                          <p>1 (10 ounce) can diced tomatoes with Hatch green chiles, drained</p>
                          <p>1/2 cup chopped cilantro or green onions for garnish</p>
                      </div>
                  </div>
                </div>

              <!-- Content Column -->
              <div class="col-12 col-lg-8 text-white p-2" data-aos="fade-up" data-aos-delay="200">
                <h2 class="text-uppercase text-center text-lg-start">Directions</h2>
                <div class="info-row">
                     <p><span class="fw-bold">Step 1:</span> <br>Preheat the oven to 350 degrees F (180 degrees C). Lightly coat a 9x13-inch baking dish with cooking spray.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 2:</span> <br>Stir baking mix, milk, and 1 tablespoon oil together in a bowl. Spread evenly in the prepared baking dish. </p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 3:</span> <br>Bake in the preheated oven until just starting to turn golden brown, 10 to 15 minutes. This step is just par-baking - this layer will not be fully baked at this point. Remove from the oven and set aside. </p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 4:</span> <br>Heat remaining tablespoon oil in a large skillet over medium-high heat. Add ground beef and cook, stirring occasionally, until almost cooked through, about 4 minutes. </p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 5:</span> <br>Add onion and poblano, stirring occasionally, until the beef is cooked through and onions begin to soften, about 4 minutes more. Stir in taco seasoning and salt until evenly combined. Remove from heat; set aside.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 6:</span> <br>Add mayonnaise, sour cream, 1 cup pepper Jack cheese, and 1 cup Cheddar cheese to a bowl; stir until well combined.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 7:</span> <br>Spoon beef mixture evenly over par-baked biscuit in the baking dish. Sprinkle drained diced tomatoes evenly over beef. Carefully spoon mayonnaise mixture over beef and tomatoes. Sprinkle evenly with remaining shredded cheese.</p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 8:</span> <br>Bake, uncovered, until cheese is bubbly and starting to turn golden brown, about 30 minutes. </p>
                </div><br>
                <div class="info-row">
                    <p><span class="fw-bold">Step 9:</span> <br>Let the dish rest for 10 minutes before serving. Garnish with sour cream and cilantro or green onions.</p>
                </div><br>
              </div>
            </div>
